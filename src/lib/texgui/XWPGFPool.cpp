static XWTeXHash * pgfKeyWordTab = 0;

static void loadPGFPool()
{
	if (!pgfKeyWordTab)
	{
		pgfKeyWordTab = new XWTeXHash(PGF_MIN_KEYWORD);
		pgfKeyWordTab->loadFile(2000, 50 * 1024, "pgf.pool");
	}
}

QString getPGFString(qint32 s)
{
	loadPGFPool();
	if (s >= PGF_MIN_KEYWORD && s <= PGFspyinnode)
		return pgfKeyWordTab->getString((qint32)s);
		
	return QString();
}

QString getPGFTranslate(int type)
{
	loadPGFPool();
	if (type >= PGF_MIN_KEYWORD && type <= PGFspyinnode)
		return qApp->translate("PGFGraphic", pgf_strings[type - PGF_MIN_KEYWORD]);
		
	return QString();
}

qint32 lookupPGFID(qint32 * buf, qint32 l)
{
	loadPGFPool();
	return pgfKeyWordTab->lookupID(buf, l);
}

qint32 lookupPGFID(const QString & str)
{
	loadPGFPool();
	return pgfKeyWordTab->lookupID(str);
}
