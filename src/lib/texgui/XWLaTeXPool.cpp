static XWTeXHash * latexKeyWordTab = 0;

static void loadLaTeXPool()
{
	if (!latexKeyWordTab)
	{
		latexKeyWordTab = new XWTeXHash(LATEX_MIN_KEYWORD);
		latexKeyWordTab->loadFile(2000, 50 * 1024, "latex.pool");
	}
}

QString getLaTeXString(qint32 s)
{
	loadLaTeXPool();
	if (s >= LATEX_MIN_KEYWORD && s <= LAAMSsymbol)
		return latexKeyWordTab->getString((qint32)s);
		
	return QString();
}

QString getLaTeXTranslate(int type)
{
	loadLaTeXPool();
	if (type >= LATEX_MIN_KEYWORD && type <= LAAMSsymbol)
		return qApp->translate("LaTeX", latex_strings[type - LATEX_MIN_KEYWORD]);
		
	return getLaTeXString(type);
}

qint32 lookupLaTeXID(qint32 * buf, qint32 l)
{
	loadLaTeXPool();
	return latexKeyWordTab->lookupID(buf, l);
}

qint32 lookupLaTeXID(const QString & str)
{
	loadLaTeXPool();
	return latexKeyWordTab->lookupID(str);
}
