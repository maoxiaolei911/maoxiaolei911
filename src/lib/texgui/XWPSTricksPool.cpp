static XWTeXHash * pstrickKeyWordTab = 0;

static void loadPSTPool()
{
	if (!pstrickKeyWordTab)
	{
		pstrickKeyWordTab = new XWTeXHash(PST_MIN_KEYWORD);
		pstrickKeyWordTab->loadFile(2000, 50 * 1024, "pstricks.pool");
	}
}

QString getPSTrickString(qint32 s)
{
	loadPSTPool();
	if (s >= PST_MIN_KEYWORD && s <= PSTpstrickskiped)
		return pstrickKeyWordTab->getString((qint32)s);
		
	return QString();
}

QString getPSTrickTranslate(int type)
{
	loadPSTPool();
	if (type >= PST_MIN_KEYWORD && type <= PSTpstrickskiped)
		return qApp->translate("PSTGraphic", pst_strings[type - PST_MIN_KEYWORD]);
		
	return QString();
}

qint32 lookupPSTrickID(qint32 * buf, qint32 l)
{
	loadPSTPool();
	return pstrickKeyWordTab->lookupID(buf, l);
}

qint32 lookupPSTrickID(const QString & str)
{
	loadPSTPool();
	return pstrickKeyWordTab->lookupID(str);
}
