static XWTeXHash * texKeyWordTab = 0;

static void loadTeXPool()
{
	if (texKeyWordTab == 0)
	{
		texKeyWordTab = new XWTeXHash(TEX_MIN_KEYWORD);
		texKeyWordTab->loadFile(1000, 10 * 1024, "tex.pool");
	}
}

QString getTexString(qint32 s)
{
	loadTeXPool();
	if (s >= TeXAbove && s < TeXMath_Spacing)
		return texKeyWordTab->getString(s);
		
	return QString();
}

QString getTexTranslate(int type)
{
	loadTeXPool();
	if (type >= TeXAbove && type < TeXMath_Spacing)
		return qApp->translate("Tex", tex_strings[type - TEX_MIN_KEYWORD]);
		
	return QString();
}

qint32 lookupTexID(qint32 * buf, qint32 l)
{
	loadTeXPool();
	return texKeyWordTab->lookupID(buf, l);
}

qint32 lookupTexID(const QString & str)
{
	loadTeXPool();
	return texKeyWordTab->lookupID(str);
}
